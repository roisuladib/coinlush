export type Locale = (typeof locales)[number]['code'];

export function formatCurrency(
   value: number | string,
   locale: Locale = 'id-ID',
   currencyCode = 'IDR',
) {
   if (!value) return '';

   const formatter = new Intl.NumberFormat(locale, {
      style: 'currency',
      currency: currencyCode,
   });

   return formatter.format(+value);
}
